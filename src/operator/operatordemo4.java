package operator;

public class operatordemo4 {
    public static void main(String[] args) {
        //赋值运算符
        /*+=    加后赋值
          -=    减后赋值
          *=    乘后赋值
          /=    除后赋值
          %=   取余后赋值
         */
        int a = 10;
        int b = 20;
        a-=b;
        System.out.println("a的结果是"+a);
    }
}
