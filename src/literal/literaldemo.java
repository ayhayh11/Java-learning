package literal;

public class literaldemo {
    public static void main(String[] args) {
        //输出恐龙的信息：霸王龙，年龄8岁，身高25米。体重2吨，性别公
        //输出恐龙名字：霸王龙
        System.out.println("恐龙名字："+"霸王龙");
        //输出恐龙年龄：8岁
        System.out.println("恐龙年龄："+8+"岁");
        //输出恐龙身高：25米
        System.out.println("恐龙身高："+25+"米");
        //输出恐龙体重：2吨
        System.out.println("恐龙体重："+2+"吨");
        //输出恐龙性别：公
        System.out.println("恐龙性别："+'公');
    }
}
